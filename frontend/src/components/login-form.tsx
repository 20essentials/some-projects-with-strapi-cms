'use client';

import { cn } from '@/lib/utils';
import { Button } from '@/components/ui/button';
import {
  Card,
  CardContent,
  CardDescription,
  CardHeader,
  CardTitle
} from '@/components/ui/card';
import {
  Field,
  FieldDescription,
  FieldGroup,
  FieldLabel
} from '@/components/ui/field';
import { Input } from '@/components/ui/input';
import Link from 'next/link';
import { formStateLogin } from '@/types/login-and-signup';
import { useActionState } from 'react';
import { actions } from '@/actions/index';
import { ErrorMessage } from './ErrorMessage';

const INITIAL_STATE: formStateLogin = {
  data: {
    email: '',
    password: '',
    identifier: undefined
  },
  message: undefined,
  strapiErrors: undefined,
  success: undefined,
  zodErrors: undefined
};

export function LoginForm({ className, ...props }: React.ComponentProps<'div'>) {
  const [formState, formAction] = useActionState(
    actions.auth.loginUserAction,
    INITIAL_STATE
  );

  // console.log({ formState });

  return (
    <div className={cn('flex flex-col gap-6', className)} {...props}>
      <Card>
        <CardHeader>
          <CardTitle>Login to your account</CardTitle>
          <CardDescription>
            Enter your email below to login to your account
          </CardDescription>
        </CardHeader>
        <CardContent>
          <form action={formAction}>
            <FieldGroup>
              <Field>
                <FieldLabel htmlFor='email'>Email</FieldLabel>
                <Input
                  id='email'
                  type='email'
                  name='email'
                  placeholder='m@example.com'
                  defaultValue={formState.data?.email}
                  required
                />
                <ErrorMessage arrayMessages={formState.zodErrors?.email} />
              </Field>
              <Field>
                <div className='flex items-center'>
                  <FieldLabel htmlFor='password'>Password</FieldLabel>
                </div>
                <Input
                  id='password'
                  type='password'
                  name='password'
                  defaultValue={formState.data?.password}
                  required
                />
                <ErrorMessage arrayMessages={formState.zodErrors?.password} />
              </Field>
              <Field>
                <Button type='submit'>Login</Button>

                <ErrorMessage
                  arrayMessages={
                    formState.strapiErrors?.message
                      ? [formState.strapiErrors?.message]
                      : undefined
                  }
                />

                <FieldDescription className='text-center'>
                  Don&apos;t have an account? <Link href='/signup'>Sign up</Link>
                </FieldDescription>
              </Field>
            </FieldGroup>
          </form>
        </CardContent>
      </Card>
    </div>
  );
}
